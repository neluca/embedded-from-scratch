#include "bit_utils.h"

/* =========================================================================
 * 端序检测
 * ========================================================================= */

int is_little_endian(void)
{
    uint16_t test = 0x0001;
    /* 小端: 低地址是 0x01, 高地址是 0x00 */
    return (*(uint8_t *)&test) == 0x01;
}

/* =========================================================================
 * 字节序反转 (M0 无 REV 指令, 纯软件实现)
 * ========================================================================= */

uint16_t swap16(uint16_t val)
{
    return (uint16_t)((val >> 8) | (val << 8));
}

uint32_t swap32(uint32_t val)
{
    return ((val >> 24) & 0x000000FF) |
           ((val >> 8) & 0x0000FF00) |
           ((val << 8) & 0x00FF0000) |
           ((val << 24) & 0xFF000000);
}

/* =========================================================================
 * 非对齐安全访问
 * =========================================================================
 * Cortex-M0 不支持非对齐的 32 位访问!
 * 访问 packed 结构体中的 uint32_t 必须逐字节读写.
 */

uint32_t packed_read_u32(const uint8_t *addr)
{
    return ((uint32_t)addr[0] << 0) |
           ((uint32_t)addr[1] << 8) |
           ((uint32_t)addr[2] << 16) |
           ((uint32_t)addr[3] << 24);
}

void packed_write_u32(uint8_t *addr, uint32_t val)
{
    addr[0] = (uint8_t)(val & 0xFF);
    addr[1] = (uint8_t)((val >> 8) & 0xFF);
    addr[2] = (uint8_t)((val >> 16) & 0xFF);
    addr[3] = (uint8_t)((val >> 24) & 0xFF);
}
